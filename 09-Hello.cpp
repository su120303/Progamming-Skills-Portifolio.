/*
    Exercise 09 - Hello
    -------------------
    This program defines a simple function called hello()
    which prints "Hello" to the console.

    Purpose: Demonstrates how to define and call a function in C++.
*/

#include <iostream>
using namespace std;

// Function definition
void hello() {
    cout << "Hello" << endl;
}

int main() {
    // Call the hello function
    hello();

    return 0;
}
