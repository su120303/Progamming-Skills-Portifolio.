/*
    Exercise 02 - Simple Sums
    -------------------------
    This program demonstrates how to use variables and arithmetic operations.
    It calculates the sum of two predefined integers and prints the result.

    Purpose: Demonstrate variable declaration, assignment, and simple arithmetic.
*/

#include <iostream>  // Required for console input/output
using namespace std;

int main() {
    // Declare and initialize two integer variables
    int num1 = 8;
    int num2 = 10;

    // Calculate their sum
    int sum = num1 + num2;

    // Output the result to the console
    cout << "The sum is: " << sum << endl;

    return 0; // End of program
}
